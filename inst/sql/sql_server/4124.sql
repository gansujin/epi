--GFR calculation by CKD-EPI

SELECT
    CASE WHEN v.gender_concept_id = 8532 -- Female calculation
        THEN 141 * POWER(CASE WHEN w.base_cr/0.7<1 THEN w.base_cr/0.7 ELSE 1 END, -0.329)*POWER(CASE WHEN w.base_cr/0.7 > 1 THEN w.base_cr/0.7 ELSE 1 END,-1.209)*POWER(0.993,CAST(FLOOR(CAST(DATEDIFF(DAY,v.birth_datetime,w.start_date) AS INTEGER)/365.2422) AS FLOAT))*1.018
        WHEN v.gender_concept_id = 8507 -- Male calculation
        THEN 141 * POWER(CASE WHEN w.base_cr/0.9<1 THEN w.base_cr/0.9 ELSE 1 END,-0.411)*POWER(CASE WHEN w.base_cr/0.9 > 1 THEN w.base_cr/0.9 ELSE 1 END,-1.209)*POWER(0.993,CAST(FLOOR(CAST(DATEDIFF(DAY,v.birth_datetime,w.start_date) AS INTEGER)/365.2422) AS FLOAT))
    END AS gfr,c.start_date, w.person_id
INTO @target_database_schema.gfr_comparator_epi
FROM @cdm_database_schema.person v
JOIN #ct_cohort_cr w ON v.person_id = w.person_id
JOIN #final_cohort c ON w.person_id = c.person_id AND w.start_date = c.start_date
WHERE CAST(FLOOR(CAST(DATEDIFF(DAY, v.birth_datetime, w.start_date) AS INTEGER) / 365.2422) AS FLOAT) > 1;

